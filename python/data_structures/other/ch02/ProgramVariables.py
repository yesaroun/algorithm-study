# Python program variables

x = 1   # integer
print("x = ", x)
print("type(x) = ", type(x))

x = 2.345   # float
print("x = ", x)
print("type(x) = ", type(x))

x = [1, 2, 3, 4]    # list
print("x = ", x)
print("type(x) = ", type(x))

x = (5, 6, 7)   # tuple
print("x = ", x)
print("type(x) = ", type(x))

x = {'A':1, 'B':2, 'C':3}   # dict
print("x = ", x)
print("type(x) = ", type(x))

x = {1, 2, 3, 4}    # set
print("x = ", x)
print("type(x) = ", type(x))
